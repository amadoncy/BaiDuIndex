def get_all_regions():
    """获取所有区域名称"""
    return list(REGIONS.keys())

def get_region_provinces(region):
    """获取指定区域下的所有省份"""
    return REGIONS.get(region, [])

def get_province_code(province):
    """获取省份代码"""
    for region_data in CITY_CODES.values():
        if province in region_data:
            return region_data[province]["code"]
    return None

def get_city_code(city):
    """获取城市代码"""
    for region_data in CITY_CODES.values():
        for province_data in region_data.values():
            if city in province_data["cities"]:
                return province_data["cities"][city]
    return None

def get_province_cities(province):
    """获取省份下的所有城市"""
    for region_data in CITY_CODES.values():
        if province in region_data:
            return list(region_data[province]["cities"].keys())
    return [] 